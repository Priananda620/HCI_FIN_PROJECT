function generateAllHeroes(){
    for (var i = 0; i < heroes.length; i++) {
        
        //variables untuk generated elements

        var aLinksOutter = document.createElement("a");

        //generate html elements
        var divOutter = document.createElement("div");
        var newImg = document.createElement("img");

        var heroesTextContent = document.createElement("div");
        var heroNameH1 = document.createElement("h1");
        var primaryAttributeH1 = document.createElement("h1");
        var allegianceH1 = document.createElement("h1");
        
        //set attribut pada tag" yang di generate tadi 
        //attributes: class,src
        $(divOutter).attr('class', 'heroes-grid-items');

        $(newImg).attr('src', heroImageUrl(heroes[i]["heroName"]));

        $(heroesTextContent).attr('class', 'heroes-text-content element-flex');

        $(aLinksOutter).attr('href', 'https://dota2.fandom.com/wiki/'+heroes[i]["heroName"]);// untuk href ke url luar

        //mengisi inner html dengan data dari api
        //inner html
        $(heroNameH1).html(heroes[i]["heroName"]);
        $(primaryAttributeH1).html(heroes[i]["primaryAttribute"]);
        $(allegianceH1).html(heroes[i]["allegiance"]);

        //append element2 tadi ke parent2nya
        //h1s
        $(heroNameH1).appendTo(heroesTextContent);
        $(primaryAttributeH1).appendTo(heroesTextContent);
        $(allegianceH1).appendTo(heroesTextContent);
        

        //wrrappers
        $(heroesTextContent).appendTo(divOutter);
        $(newImg).appendTo(divOutter);

        $(divOutter).appendTo(aLinksOutter);
        //append to outter wrapper
        $(aLinksOutter).appendTo("#heroes-grid-container");

        // contoh struktur html ada di heroes.html di dalam div#heroes-grid-container

        // newImg.setAttribute("src", concatHeroImageUrl(heroes[i]["heroName"]));
        

        // newImg.setAttribute("class", 'heroes-grid-items');

        // $(newImg).appendTo("#heroes-grid-container");
    }
    console.log(heroes.length);

}


function heroImageUrl(heroName) {
    
    var randString;

    //agar ratio gambar random, gallery terlihat lebih aesthetic. Karena di cdn nya juga sudah tersedia gambar yang portrait dan landscape
    if((Math.floor(Math.random() * 5)) % 2 == 0){
        randString = "http://cdn.dota2.com/apps/dota2/images/heroes/" + heroName + "_" + 'vert' + ".jpg";
    }else{
        randString = "http://cdn.dota2.com/apps/dota2/images/heroes/" + heroName + "_" + 'full' + ".png";
    }

    return randString;
    
    // return "https://cdn.cloudflare.steamstatic.com/apps/dota2/images/dota_react/heroes/" + heroName + ".png"
}